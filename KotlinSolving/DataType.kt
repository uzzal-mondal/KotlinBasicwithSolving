package MayKotlinSolving.Basic

fun main(args:Array<String>){

   // Datatype in Kotlin

    //Int
    var num1 :Int = 10;
    println("Integer $num1")

    //Byte
    var bt:Byte = 25;
    println("Byte $bt")

    //Short
    var s:Short = 25;
    println("Short $s")

    //Long
    var l:Long = 120L
    println("Long $l")

    //Float
    var fl: Float = 10.25F
    println("Float : $fl")

    //Double
    var db:Double = 120.35;
    println("Double : $db")

    //Char
    var ch : Char = 'j'
    println("Char : $ch")


    //String
    var name:String = "Techoners"
    println("Name : $name")

    //Boolean
    var bl : Boolean  //defining variable value
    bl = true      // asining variable value
    println("Boolean : $bl")



}